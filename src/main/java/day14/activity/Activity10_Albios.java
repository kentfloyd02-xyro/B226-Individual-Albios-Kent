public class Activity10_Albios {

    public static void main(String[] args) {
        //        Math.abs()
//      The Math.abs() method returns the absolute (positive) value of a number.

        double num1 = -25.5;
        System.out.println("The absolute value is: " + Math.abs(num1)); // Math.abs(number);

//        Math.max()
//      The Math.max() method returns the larger of two values.
        int a = 10, b = 20;
        System.out.println("The largest value is: " + Math.max(a, b)); // Math.max(value1, value2);

//        Math.min()
//      The Math.min() method returns the smaller of two values.
        int c = 15, d = 8;
        System.out.println("The smallest value is: " + Math.min(c, d)); // Math.min(value1, value2);

//        Math.pow()
//      The Math.pow() method raises a number to a power.
        System.out.println(Math.pow(2, 3)); // Math.pow(base, exponent)

//        Math.sqrt()
//      The Math.sqrt() method returns the square root of a number.
        System.out.println(Math.sqrt(64)); // Math.sqrt(number)

//        Math.round()
//      The Math.round() method rounds to the nearest integer.
        System.out.println(Math.round(5.6)); // Math.round(number)

//        Math.ceil()
//      The Math.ceil() method rounds up to the nearest whole number.
        System.out.println(Math.ceil(4.2)); // Math.ceil(number)

//        Math.floor()
//      The Math.floor() method rounds down to the nearest whole number.
        System.out.println(Math.floor(4.9)); // Math.floor(number)

//        Math.random()
//      The Math.random() method generates a random number between 0.0 and 1.0.
        System.out.println(Math.random()); // Math.random()

//        Math.log()
//      The Math.log() method returns the natural logarithm (base e).
        System.out.println(Math.log(10)); // Math.log(number)

//        Math.log10()
//      The Math.log10() method returns the base 10 logarithm.
        System.out.println(Math.log10(100)); // Math.log10(number)

//        Math.exp()
//      The Math.exp() method returns e raised to the power of a number.
        System.out.println(Math.exp(2)); // Math.exp(number)

//        Math.sin()
//      The Math.sin() method returns the sine of an angle in radians.
        System.out.println(Math.sin(Math.PI / 2)); // Math.sin(radians)

//        Math.cos()
//      The Math.cos() method returns the cosine of an angle in radians.
        System.out.println(Math.cos(0)); // Math.cos(radians)

//        Math.tan()
//      The Math.tan() method returns the tangent of an angle in radians.
        System.out.println(Math.tan(Math.PI / 4)); // Math.tan(radians)


        /*
    Method              Purpose
    Math.abs()          Returns absolute value
    Math.max()          Returns larger value
    Math.min()          Returns smaller value
    Math.pow()          Raises number to a power
    Math.sqrt()         Returns square root
    Math.round()        Rounds to nearest integer
    Math.ceil()         Rounds up
    Math.floor()        Rounds down
    Math.random()       Generates random number (0.0–1.0)
    Math.log()          Natural logarithm (base e)
    Math.log10()        Logarithm base 10
    Math.exp()          Exponential function (e^x)
    Math.sin()          Sine of angle (radians)
    Math.cos()          Cosine of angle (radians)
    Math.tan()          Tangent of angle (radians)
         */
    }
}
